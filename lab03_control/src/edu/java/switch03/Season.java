package edu.java.switch03;

// 열거형 타입 (enumeration type): 한정된 값(상수)들만을 갖는 데이터 타입.
// 예: 계절(봄, 여름, 가을, 겨울), 식사 시간(아침, 점심, 저녁), 요일(일, 월,...., 토)


//봄 여름 가을 겨울 -> Season이라는거 안에, 계절값들을 나열한것.
public enum Season {
    SPRING, SUMMER, FALL, WINTER; // 값이 변할 수 없는것들은 상수로써 대문자로 표현하는것이 자바관습
    
    
}
